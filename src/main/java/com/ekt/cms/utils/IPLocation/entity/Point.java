/**
 * 坐标位置
 * @author wanglan
 * 2017年3月28日下午8:06:14
 */
public class Point {
	
	private double x;//x坐标
	private double y;//y坐标
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	

}
